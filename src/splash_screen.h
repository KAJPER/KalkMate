#define _SPL_LOGO_W  48
#define _SPL_LOGO_H  56

// ---------------------------------------------------------------------------
// Ekran powitalny z animacja 3-fazowa
// Uklad: logo (48x56) po lewej + tekst po prawej
//
//  Ekran 256x64:
//   Logo: x=4,  y=4  (48x56)
//   Tekst x=60 (4+48+8)
//   "KalkMate"   y=24  font 10x20
//   "AI Math Solver" y=42  font 5x7
//   Pasek postepu: y=56, x=60..252
// ---------------------------------------------------------------------------
void showSplashScreen(U8G2 &display) {
    const int LX = 4;    // logo X
    const int LY = 4;    // logo Y
    const int TX = 60;   // tekst X
    const int BAR_Y = 59;
    const int BAR_W = 192; // 256-60-4

    // --- Faza 1: samo logo wjezdza od lewej ---
    for (int x = -_SPL_LOGO_W; x <= LX; x += 4) {
        display.clearBuffer();
        display.drawXBMP(x, LY, _SPL_LOGO_W, _SPL_LOGO_H, KALKMATE_LOGO);
        display.sendBuffer();
        delay(16);
    }
    // Upewnij sie ze logo jest dokladnie na miejscu
    display.clearBuffer();
    display.drawXBMP(LX, LY, _SPL_LOGO_W, _SPL_LOGO_H, KALKMATE_LOGO);
    display.sendBuffer();
    delay(300);

    // --- Faza 2: "KalkMate" wjezdza od prawej ---
    for (int x = 256; x >= TX; x -= 6) {
        display.clearBuffer();
        display.drawXBMP(LX, LY, _SPL_LOGO_W, _SPL_LOGO_H, KALKMATE_LOGO);
        display.setFont(u8g2_font_10x20_tf);
        display.drawStr(x, 26, "KalkMate");
        display.sendBuffer();
        delay(16);
    }
    delay(200);

    // --- Faza 3: podtytul pojawia sie + pasek postepu ---
    // Podtytul fade-in (symulacja przez miganie → pojawia sie)
    display.clearBuffer();
    display.drawXBMP(LX, LY, _SPL_LOGO_W, _SPL_LOGO_H, KALKMATE_LOGO);
    display.setFont(u8g2_font_10x20_tf);
    display.drawStr(TX, 26, "KalkMate");
    display.setFont(u8g2_font_5x7_tf);
    display.drawStr(TX, 40, "AI calculator");
    display.sendBuffer();
    delay(300);

    // Pasek postepu rosnie od lewej do prawej
    display.drawFrame(TX, BAR_Y - 4, BAR_W, 5);
    display.sendBuffer();

    for (int w = 0; w <= BAR_W - 2; w += 3) {
        display.clearBuffer();
        display.drawXBMP(LX, LY, _SPL_LOGO_W, _SPL_LOGO_H, KALKMATE_LOGO);
        display.setFont(u8g2_font_10x20_tf);
        display.drawStr(TX, 26, "KalkMate");
        display.setFont(u8g2_font_5x7_tf);
        display.drawStr(TX, 40, "AI calculator");
        // Kontur paska
        display.drawFrame(TX, BAR_Y - 4, BAR_W, 5);
        // Wypelnienie
        if (w > 0) display.drawBox(TX + 1, BAR_Y - 3, w, 3);
        display.sendBuffer();
        delay(12);
    }

    // Pelny pasek + krotka pauza
    display.clearBuffer();
    display.drawXBMP(LX, LY, _SPL_LOGO_W, _SPL_LOGO_H, KALKMATE_LOGO);
    display.setFont(u8g2_font_10x20_tf);
    display.drawStr(TX, 26, "KalkMate");
    display.setFont(u8g2_font_5x7_tf);
    display.drawStr(TX, 40, "AI calculator");
    display.drawBox(TX, BAR_Y - 4, BAR_W, 5);
    display.sendBuffer();
    delay(600);
}
